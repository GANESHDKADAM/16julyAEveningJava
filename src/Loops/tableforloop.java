package Loops;

public class tableforloop 
{

	public static void main(String[] args) 
	{
		for(int i=2; i<=20; i++)
		{
			System.out.println(i);
			i=i+1;
		}
		System.out.println("=========================================");
		for(int j=5;j<=50;j++)
		{
			System.out.println(j);
			j=j+4;
			
			
		}

	}

}
