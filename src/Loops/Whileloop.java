package Loops;

public class Whileloop {

	public static void main(String[] args) 
	{
		// we want to create 1 to 100 number in increamental process
		// start....>1   condition.....<=100    updation
		int i=1;
		while(i<=100)
		{
			System.out.println(i);
			i++;
		}
		
		// we want to create 1 to 100 number in increamental process
				// start....>1   condition.....<=100    updation
		System.out.println("===========================================");
				int j=100;
				while(j>=1)
				{
					System.out.println(j);
					j--;
				}
				
		

	}

}
