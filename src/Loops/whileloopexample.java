package Loops;

public class whileloopexample {

	public static void main(String[] args) 
	{
		// start....7     condition....70    updation
		
		int a=7;
		while(a<=70)
		{
			System.out.println(a);
			a=a+7;
		}
		
		System.out.println("=========================");
		int b=70;
		while(b>=7)
		{
			System.out.println(b);
			b=b-7;
			
		}

	}

}
