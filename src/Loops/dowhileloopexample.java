package Loops;

public class dowhileloopexample {

	public static void main(String[] args) 
	{
		// start ....6   condition <=60   updation
		
		int c=6;
		do
		{
			System.out.println(c);
			c=c+6;
		}
		while(c<=60);
		
		
		System.out.println("=========================");
		
		int d=60;
		do
		{
			System.out.println(d);
			d=d-6;
		}
		while(d>=6);

	}

}
