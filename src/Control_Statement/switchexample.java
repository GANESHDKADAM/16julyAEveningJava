package Control_Statement;

public class switchexample {

	public static void main(String[] args)
	{
	  
		String Villagename="undirwadi";
		switch(Villagename)
		{
		case "pune":System.out.println("i am pune kar");
		break;
		case "mumbai":System.out.println(" i am mumbai kar");
		break;
		case "waluj": System.out.println("i am walujkar");
		break;
		case "undirwadi":System.out.println("i am undirwadi kar");
		break;
		default :System.out.println("out of city");
		break;
		}
		

	}

}
