package Control_Statement;

public class switch_Study {

	public static void main(String[] args) {
		
		String city="lasur";
		
		switch(city)
		{
		case "Pune": System.out.println(" i am punekar");
		break;
		case "mumbai": System.out.println(" i am mumbaikar");
		break;
		case "nagar": System.out.println("i am nagarkar");
		break;
		case "undirwadikar": System.out.println(" i am undirwadi kar");
		break;
		default: System.out.println("not in our list");
		break;
		
		}
		
		

	}

}
