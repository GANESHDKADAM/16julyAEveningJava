package Control_Statement;

public class switchpractice 
{

	public static void main(String[] args) 
	{
		String city="parbhani";
		switch(city)
		{
		case "Lasur":System.out.println("i am lasur kar");
		break;
		case "waluj":System.out.println("i am walujkar");
		break;
		case "parbhani":System.out.println("i am parbhanikar");
		break;
		case "aurangabad":System.out.println("i am aurangabadkar");
		break;
		default:System.out.println("out of city");
		break;
		}

	}

}
